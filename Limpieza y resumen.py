# Recalcular las columnas numéricas del DataFrame limpio
numeric_cols = data_limpia.select_dtypes(include=[np.number]).columns
correlation = data_limpia[numeric_cols].corr()
print("Correlación entre variables numéricas:")
correlation

# 10) IDENTIFICAR LAS VARIABLES CON MAYOR CORRELACIÓN
high_corr = correlation.abs().unstack().sort_values(kind="quicksort", ascending=False)

# Eliminar la autocorrelación (corr de una variable consigo misma)
high_corr = high_corr[high_corr != 1]
print("Pares de variables con alta correlación:")
print(high_corr.head(10))

# Crear una máscara que seleccione solo el triángulo superior (sin la diagonal)
mask = np.triu(np.ones_like(correlation, dtype=bool), k=1)
unique_corr = correlation.where(mask).stack().abs().sort_values(ascending=False)

print("Pares de variables con alta correlación (sin duplicados):")
print(unique_corr.head(10))

