data

# 3) ORDENAR POR 'Pais' Y 'Año'
data.sort_values(by=['Pais', 'Año'], inplace=True)
data

# 4) LISTAR LAS COLUMNAS NUMÉRICAS
numeric_cols = data.select_dtypes(include=[np.number]).columns
numeric_cols

# 5) AGRUPAR POR 'Pais'
grupos = data.groupby('Pais')
resultados = []  # aquí iremos guardando cada grupo procesado

for pais, df_pais in grupos:
    # df_pais contiene solo las filas de un país

    # a) Ordenar dentro del país por 'Año'
    df_pais = df_pais.sort_values(by='Año')

    # b) Interpolar cada columna numérica
    for col in numeric_cols:
        df_pais[col] = df_pais[col].interpolate(method='linear', limit_direction='both')

    # c) Rellenar valores faltantes restantes con la mediana local (del mismo país)
    for col in numeric_cols:
        # Si queda NaN, lo llenamos con la mediana de ese grupo
        df_pais[col].fillna(df_pais[col].median(), inplace=True)

    # Agregar el DataFrame del país ya limpio a la lista
    resultados.append(df_pais)

# 6) CONCATENAR TODOS LOS GRUPOS
data_limpia = pd.concat(resultados, ignore_index=True)
data_limpia

# 7) VERIFICAR SI QUEDAN VALORES FALTANTES
print("¿Hay valores NA en el dataset?:", data_limpia.isna().any().any())
